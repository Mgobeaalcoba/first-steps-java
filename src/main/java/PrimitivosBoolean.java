public class PrimitivosBoolean {
    public static void main(String[] args) {
        // Primitivos booleanos
        boolean datoLogico = true;
        boolean datoLogico2 = false;
        System.out.println("datoLogico = " + datoLogico);
        System.out.println("datoLogico2 = " + datoLogico2);
        System.out.println("true tipo boolean corresponde en byte a " + Byte.BYTES);
        System.out.println("true tipo boolean corresponde en bits a " + Byte.SIZE);
        System.out.println("El valor mínimo de un boolean es " + Boolean.FALSE);
        System.out.println("El valor máximo de un boolean es " + Boolean.TRUE);
    }
}
